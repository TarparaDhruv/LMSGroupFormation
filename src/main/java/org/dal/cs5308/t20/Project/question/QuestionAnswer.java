package org.dal.cs5308.t20.Project.question;

public class QuestionAnswer {
	private String answer;
	private int value;
	
	public QuestionAnswer(String answer, int value) {
		this.answer = answer;
		this.value = value;
	}

	public String getAnswer() {
		return answer;
	}

	public int getValue() {
		return value;
	}

}
